'''
1.4.    Inicializar un string con la cadena "mAriA" luego llamar a sus m√©todos upper(),
        lower() y capitalize(), guardar los datos retornados en otros string y mostrarlos por
        pantalla.

'''
nombre="mAriA"

nombre_mayus=nombre.upper()
nombre_min=nombre.lower()
nombre_cap=nombre.capitalize()

print(f'Mayusculas: {nombre_mayus}, \nMinuscula: {nombre_min}, \nLetra Capital: {nombre_cap}' )
